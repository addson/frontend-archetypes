
/* 05 Operações Aritméticas */

// Declarações
var a = 10;
var b = 15;

// Adição
var funcao = a + b;
console.log("Soma: " + funcao);

// Subtração
funcao = b - a;
console.log("Subtracao: " + funcao);

// Multiplicação
funcao = a * b;
console.log("Multiplicacao: " + funcao);

// Divisão
funcao = b / 5;
console.log("Divisao: " + funcao);

// Módulo (o resto da divisão)
funcao = b % 2;
console.log("Modulo: " + funcao);

// Valor de a
console.log("a: " + a)

// Incremento Após
funcao = a++;
console.log("Incremento de a Após a Atribuição: " + funcao);

// Valor de b
console.log("b: " + b)

// Decremento Após
funcao = b--;
console.log("Decremento de b Após a Atribuição: " + funcao);

// Valor de a
console.log("a: " + a)

// Incremento Antes
funcao = ++a;
console.log("Incremento de a Antes da Atribuição: " + funcao);

// Valor de b
console.log("b: " + b)

// Decremento Antes
funcao = --b;
console.log("Decremento de b Antes da Atribuição: " + funcao);


